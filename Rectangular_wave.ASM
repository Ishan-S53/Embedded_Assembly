ORG 0000H
SJMP START
ORG 0035H
START:
CLR P1.1
MOV TMOD,#01H

REPEAT:SETB P1.1
ACALL DELAY_1
CPL P1.1
ACALL DELAY_2
LJMP REPEAT

ORG 0200H
DELAY_1: MOV TH0,#0FDH
MOV TL0,#44H
SETB TR0
CHECK_1: JNB TF0,CHECK_1
CLR TR0
CLR TF0
RET

DELAY_2: MOV TH0,#0FEH
MOV TL0,#0D4H
SETB TR0
CHECK_2: JNB TF0,CHECK_2
CLR TR0
CLR TF0
RET

END