ORG 0000H
SJMP MAIN
ORG 0040H
MAIN:
MOV P1, #00H
MOV P2, #00H
MOV A, #38H
ACALL COMMAND
MOV A, #0EH
ACALL COMMAND
MOV A, #80H
ACALL COMMAND
MOV A, #01H
ACALL COMMAND
MOV DPTR, #WORD1
MOV A, #086H
ACALL COMMAND
REPEAT:
MOV A, #00H
MOVC A, @A+DPTR
JZ FINISH1
ACALL DATA1
INC DPTR
SJMP REPEAT
FINISH1:
MOV DPTR, #WORD2
MOV A, #0C5H
ACALL COMMAND
REPEAT1:
MOV A, #00H
MOVC A, @A+DPTR
JZ FINISH
ACALL DATA1
INC DPTR
SJMP REPEAT1
FINISH:
SJMP FINISH
COMMAND:
ACALL DELAY
MOV P1, A
CLR P2.0
SETB P2.1
CLR P2.1
RET
DATA1:
ACALL DELAY
MOV P1, A
SETB P2.0
SETB P2.1
CLR P2.1
RET
DELAY:
MOV R0, #0FFH
L1:
MOV R1, #0FFH
L2:
DJNZ R1, L2
DJNZ R0, L1
RET
WORD1:
DB 'IIITN', 00H
WORD2:
DB 'ECE', 'DEPT', 00H
END